class Wind {
  Wind({
    this.speed,
    this.deg,
    this.gust,
  });

  double speed;
  int deg;
  double gust;

  factory Wind.fromJson(Map<String, dynamic> json) => Wind(
        speed: (json["speed"] == null) ? 0.0 : json["speed"].toDouble(),
        deg: json["deg"],
        gust: (json["gust"] == null) ? 0.0 : json["gust"].toDouble(),
